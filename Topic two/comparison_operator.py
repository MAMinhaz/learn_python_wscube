a = 10
b = 120

print(a == b)       #comparison operator for equal
print(a != b)       #comparison operator for not equal

print(a>b)          #comparison operator for grater than
print(a<b)          #comparison operator for smaller than

print(a>=b)         #comparison operator for grater than or equal
print(a<=b)         #comparison operator for smaller than or equal
